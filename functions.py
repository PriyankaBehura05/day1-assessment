def add(a, b):
    return a + b  #addition

def subtract(a, b):
    return a - b   #substraction

def multiply(a, b):
    return a * b     #multiplication

def divide(a, b):
    return a / b   #division

num1 = 30    # input values for testing 
num2 = 20

print("Addition:", add(num1, num2))
print("Subtraction:", subtract(num1, num2))
print("Multiplication:", multiply(num1, num2))
print("Division:", divide(num1, num2))

# output :
# Addition: 50
# Subtraction: 10
# Multiplication: 600
# Division: 1.5

